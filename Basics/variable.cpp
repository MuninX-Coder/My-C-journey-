#include <iostream>
using namespace std;

int main(){
    // variables - variables are container where we can store data
    int x = 45;  // int - data type , x - variable name ,  45 - value
    cout << x << endl;

    cout << x + 45 << endl;

    int y = 56;
    cout << x + y << endl;
    cout << x * y << endl;
    cout << x - y << endl;

    int num;  // declaration
    num = 34; // initilization
    cout << num << endl;

    int num1  =34, num2 = 54;  // in one line
    cout << num1 + num2 << endl;

    // data type - basically what type of data
    // int - integers (numbers)
    // float - deciaml point value use f 
    // char - character use single quote 'x'
    // string - sentence use double quote "sentence "
    // double - large decimal point value use l  

    int new_var_age = 18;
    cout << "My age : " << new_var_age << endl;

    int my_class = 12;
    cout << "My class : " << my_class << endl;

    int my_rollno = 38;
    cout << "My roll no : " << my_rollno << endl;

    return 0;
}