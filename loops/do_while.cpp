#include <iostream>
using namespace std;

int main()
{
    // ek bar tho code jarur chalega
    int i = 1;
    do
    {
        cout << i << endl;
        i++;
    } while (i <= 10);



    // infinite loop
    int p = 1;

    while (p = 10)
    {
        cout << p << endl;
        p = p + 1;
    }

    return 0;
}